/**
 * يبني كتلة نصية بالمراجع المسموح للنموذج الاستشهاد بها لهذه القيمة.
 * القيم المعروفة تحصل على مراجعها الخاصة إضافةً إلى البنك العام؛ والقيم
 * المخصّصة تحصل على البنك العام فقط.
 */
export function buildReferenceBlock(value: string): string {
  const known = REFERENCES[value.trim()];
  const verses = [...(known?.verses ?? []), ...GENERAL_BANK.verses];
  const hadiths = [...(known?.hadiths ?? []), ...GENERAL_BANK.hadiths];

  const versesList = verses.map((v) => `  - ${v}`).join("\n");
  const hadithsList = hadiths.map((h) => `  - ${h}`).join("\n");

  return `المراجع الموثّقة المسموح بالاستشهاد بها (لهذه القصة فقط):

الآيات:
${versesList}

الأحاديث:
${hadithsList}

قاعدة صارمة: لا تستشهد بأي آية أو حديث إلا نصًّا واحدًا أو نصّين من القائمة أعلاه، منقولًا بحروفه كما ورد تمامًا. يُمنَع منعًا باتًا الاستشهاد بأي آية أو حديث غير موجود في هذه القائمة، ويُمنَع تعديل ألفاظ النص أو دمج نصّين. اختر الأنسب للموقف والقيمة، وضعه في موضع طبيعي على لسان أحد الكبار. إن لم يناسب أيٌّ منها سياق القصة، فاذكر معنى القيمة بأسلوبك دون وضع أي نص بين علامتي تنصيص ودون نسبته إلى القرآن أو إلى النبي ﷺ.`;
}

/**
 * يبني كتلة نصية بالمراجع المقترحة الموثّقة لهذه القيمة.
 * القيم المعروفة تحصل على مراجعها الخاصة إضافةً إلى البنك العام؛ والقيم
 * المخصّصة تحصل على البنك العام فقط.
 */
function shuffle<T>(arr: T[]): T[] {
  const a = [...arr];
  for (let i = a.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [a[i], a[j]] = [a[j], a[i]];
  }
  return a;
}

export function buildReferenceBlock(value: string): string {
  const known = REFERENCES[value.trim()];
  // النصوص الخاصة بالقيمة أولًا ثم العام، مع تدوير عشوائي في كل طلب لمنع
  // تكرار النص نفسه بين القصص وتشجيع النموذج على التنويع.
  const verses = [...shuffle(known?.verses ?? []), ...shuffle(GENERAL_BANK.verses)];
  const hadiths = [...shuffle(known?.hadiths ?? []), ...shuffle(GENERAL_BANK.hadiths)];

  const versesList = verses.map((v) => `  - ${v}`).join("\n");
  const hadithsList = hadiths.map((h) => `  - ${h}`).join("\n");

  return `مراجع مقترحة موثّقة (مضمونة المطابقة، لك أن تختار منها أو من غيرها بشرط الصحة):

الآيات:
${versesList}

الأحاديث (كلها من الصحيحين):
${hadithsList}

طريقة الاختيار (مهمة لمنع التكرار): قبل أن تكتب، استحضر ذهنيًا ثلاثة أو أربعة نصوص صحيحة **مختلفة** تخصّ هذه القيمة مباشرة (من هذه القائمة أو من معرفتك بالقرآن والصحيحين)، ثم اختر منها النص الأنسب لأحداث هذه القصة تحديدًا — وتعمّد أن يكون مختلفًا عمّا يتبادر أولًا وعن النص الأشهر، ما دام صحيحًا ومرتبطًا بالقيمة. ضع النص في المشهد المحوري الذي تتجلى فيه القيمة لا على حدث فرعي. إن كانت القيمة سلوكًا محددًا لا تجد له نصًّا مباشرًا، فاختر أقرب نص إلى أصلها ومعناها واربطه بها صراحةً. اختر ما يخدم القيمة بصدق لا لمجرد الحشو.`;
}
